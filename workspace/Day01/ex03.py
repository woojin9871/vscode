# 변수 선언
a = 10
b = 12.34
c = '김휴먼'

print( type(a) )
print( type(b) )
print( type(c) )

x = 10
y = 20

print("x + y = ", x + y)
print("x - y = ", x - y)
print("x * y = ", x * y)
print("x / y = ", x / y)
print("x % y = ", x % y)

# 정수 나누기
m = 10
n = 3
print("m // n = ", m // n)

# 제곱
i = 2
j = 3
print("2의 제곱 : ", i ** j)


# 연산자는 곱셈, 나눗셈이 덧셈, 뺄셈보다 우선한다.
print("1 + 2 * 3 = ", 1 + 2 * 3 )
print("(1 + 2) * 3 = ", (1 + 2) * 3 )
print("1 + (2 * 3) = ", 1 + (2 * 3) )